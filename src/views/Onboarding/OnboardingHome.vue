import { mapActions } from 'vuex'

import LogoWallet from '@/assets/icons/logo_wallet.svg'

export default {
  components: {
    LogoWallet
  },
  data: function () {
    return {
      analyticsAccepted: true,
      scrolledToEnd: false
    }
  },
  methods: {
    ...mapActions(['acceptTermsAndConditions']),
    async acceptTnC () {
      await this.acceptTermsAndConditions({
        analyticsAccepted: this.analyticsAccepted
      })
      this.$router.push(`/onboarding/${this.$route.query?.isImport ? 'import' : 'setup'}`)
    },
    setAnalyticsOption (accepted) {
      this.analyticsAccepted = accepted
    },
    handleScroll (el) {
      if (el.target) {
        const { offsetHeight, scrollTop, scrollHeight } = el?.target
        const scrolled = offsetHeight + scrollTop
        if (scrolled >= scrollHeight - 50) { // (50) is buffer
          this.scrolledToEnd = true
        } else {
          this.scrolledToEnd = false
        }
      }
    }
  }
}
</script>

